def write_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=False) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def read_json_if_exists(path: Path) -> dict[str, Any]:
    if not path.is_file():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def read_contract_row() -> dict[str, Any]:
    manifest = read_json_if_exists(root() / "generated" / "contract_manifest.json")
    for row in manifest.get("contract", {}).get("rows", []):
        if row.get("artifact_id") == "02-target-state-deprecation-map-md":
            return row
    raise RuntimeError("contract row not found for 02-target-state-deprecation-map-md")


