def summarize(entries: list[dict[str, Any]]) -> dict[str, int]:
    out = {"retired": 0, "replaced": 0, "wrapped": 0, "preserved": 0}
    for item in entries:
        out[item["classification"]] = out.get(item["classification"], 0) + 1
    return out


def render_markdown(payload: dict[str, Any]) -> str:
    rows = [
        "| component | classification | disposition | replacement | owner | risk |",
        "|---|---|---|---|---|---|",
    ]
    for item in payload["entries"]:
        safe = {key: str(value).replace("|", "\\|") for key, value in item.items()}
        rows.append(
            "| {component} | {classification} | {disposition} | {replacement} | {owner} | {risk} |".format(**safe)
        )
    evidence_rows = [
        "| component id | evidence refs |",
        "|---|---|",
    ]
    for item in payload["entries"]:
        refs = ", ".join(f"`{ref}`" for ref in item["evidence_refs"])
        evidence_rows.append(f"| `{item['component_id']}` | {refs} |")
    summary = payload["summary"]
    return "\n".join(
        [
            "# Deprecation Map",
            "",
            f"- Task: `{TASK_ID}`",
            "- Contract artifact: `artifact:02-target-state-deprecation-map-md`",
            f"- Canonical path: `{CANONICAL_MD}`",
            f"- Generated at: `{payload['generated_at']}`",
            f"- Components: {len(payload['entries'])}",
            f"- Retired: {summary['retired']}; replaced: {summary['replaced']}; wrapped: {summary['wrapped']}; preserved: {summary['preserved']}",
            "",
            "## Map",
            "",
            *rows,
            "",
            "## Evidence",
            "",
            *evidence_rows,
            "",
            "## Validation Links",
            "",
            "- Depends on `REQ-024_ENVCTL_ARTIFACT_REGISTRY` for path and hash registration.",
            "- Depends on `REQ-040_SHARED_PROTOCOL_SCHEMAS` for structured protocol compatibility.",
            "- Blocks `VER-300_UNIT_VALIDATION` until the artifact file, registry hash, and validation links are present.",
            "",
        ]
    )


